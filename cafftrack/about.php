    <h3>About CaffTrack</h3>
    <p>
        CaffTrack is a web-based caffeine intake monitoring system designed to help users 
        track daily caffeine consumption from coffee, tea, soft drinks, and energy drinks.
        It helps users understand their intake patterns and stay within healthy caffeine limits.
    </p>

    <img src="https://images.unsplash.com/photo-1495474472287-4d71bcdd2085?auto=format&fit=crop&w=900&q=80" alt="Caffeine Drinks" class="about-img">

    <h3>Why Caffeine Monitoring Matters</h3>
    <p>
        Excess caffeine intake can lead to sleep problems, increased heart rate, anxiety, 
        dehydration, and reduced concentration. Monitoring caffeine helps users make better
        lifestyle choices.
    </p>

    <h3>Project Objective</h3>
    <p>
        The objective of this project is to provide a simple platform where users can 
        record caffeine intake, monitor daily consumption, and receive alerts if intake 
        exceeds recommended safe limits.
